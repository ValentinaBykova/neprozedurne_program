changes :: [a] -> [a]
changes [] = []
changes [x] = [x]
changes (x:xs) = changes xs++ [x]

-- Результат тестування:
-- Prelude> changes [1,2,3,4,5]
-- > [5,4,3,2,1]

--б)
reverses :: [a] -> [a]
reverses = reverse

-- Результат тестування:
-- Prelude> reverses "afgh123"
