-- Завдання 2.Перемішування списку з n елементів: після першого розмістити останній,
-- після другого передостанній тощо. Наприклад вхідний список "abcde1234" має бути
-- трансформований у "a4b3c2d1e"

--а)
mixes :: [a] -> [a]
mixes [] = []
mixes [x] = [x]
mixes (x:xs) = [x] ++ mixes(changes xs)

-- Результат тестування:
-- Prelude>  mixes [1,2,3,4,5]
-- > [1,5,2,4,3]

--б)
mixs :: [a] -> [a]
mixs [] = []
mixs [x] = [x] 
mixs (x:xs) = [x] ++ mixs(reverse xs)  

-- Результат тестування:
-- Prelude> mixs "afgh123"
-- > "a3f2g1h"

